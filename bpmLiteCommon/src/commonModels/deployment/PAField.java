package commonModels.deployment;


public class PAField {

	private String name;
	private String type;
	private String initalData;
	private int fieldId;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getInitalData() {
		return initalData;
	}
	public void setInitalData(String initalData) {
		this.initalData = initalData;
	}
	public int getFieldId() {
		return fieldId;
	}
	public void setFieldId(int fieldId) {
		this.fieldId = fieldId;
	}
	
	
}
